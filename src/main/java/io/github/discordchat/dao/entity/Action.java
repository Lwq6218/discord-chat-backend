    private Long profileId;

    /**
     * 问题ID
     */
    private Long questionId;

    /**
     * 动作类型 0-收藏 1-赞 2-踩
     */
    private Integer actionType;

    /**
     * 回复ID
     */
    private Long answerId;

    /**
     * 创建时间
     */
    private LocalDateTime createTime;

    /**
     * 更新时间
     */
    private LocalDateTime updateTime;


